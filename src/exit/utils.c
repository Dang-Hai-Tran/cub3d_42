#include "cub3d.h"

void	free_arr(void **arr)
{
	size_t		i;

	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		i++;
	}
	if (arr)
	{
		free(arr);
		arr = NULL;
	}
}
